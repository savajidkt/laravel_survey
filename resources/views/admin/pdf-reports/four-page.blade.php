            Establishing Rapport
          </div>
          <p style="font-size:14px; line-height:20px;">The ability to use <b>ENERGY</b> to create a positive initial connection when meeting someone for the first time. A person’s ability to effectively establish rapport with others often determines how well their relationships begin. Energy sits at the center of this skill — people who can consistently give positive energy are often more successful at easing into conversations and set great foundations for building sustainable, long-term relationships. Your energy speaks even before your words do.</p>
