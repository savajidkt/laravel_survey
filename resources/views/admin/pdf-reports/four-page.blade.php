          Developing Trust
          </div>
          <p style="font-size:14px; line-height:20px;">The ability to be <b>VULNERABLE</b> and risk being exposed to the actions and behaviors of others. Often, leaders observe and evaluate their teams to identify those who are “trustworthy,” while neglecting the fact that genuine trust must be given, not earned. Leaders who truly develop trust with others are willing to be vulnerable. When vulnerability is practiced consistently, it helps establish a psychologically safe environment for relationships to flourish and grow.</p>
