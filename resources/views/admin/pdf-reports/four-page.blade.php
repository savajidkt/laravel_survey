  <h2 class="page_head">Dimensions</h2>

  <p style="font-size:15px; line-height:20px;">Through his research and practice over the last 20 years, Dr. Bandelli has identified the five essential skills of Relational Intelligence. Each of these five skills plays an important role in how a leader builds relationships with their people. Mastering these skills has been found to empower leaders to create positive, long-lasting impact on their teams and at their organizations.</p><br><br>



